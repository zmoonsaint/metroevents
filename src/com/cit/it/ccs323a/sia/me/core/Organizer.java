package com.cit.it.ccs323a.sia.me.core;

public class Organizer {
	
	private String orgUsername;
	private String orgEmail;
	private String orgEvent;
	private String orgPassword;
	private String orgRequest;
	
	public String getOrgUsername() {
		return orgUsername;
	}

	public void setOrgUsername(String orgUsername) {
		this.orgUsername = orgUsername;
	}


	public String getOrgEvent() {
		return orgEvent;
	}


	public void setOrgEvent(String orgEvent) {
		this.orgEvent = orgEvent;
	}


	public String getOrgEmail() {
		return orgEmail;
	}


	public void setOrgEmail(String orgEmail) {
		this.orgEmail = orgEmail;
	}


	public String getOrgPassword() {
		return orgPassword;
	}


	public void setOrgPassword(String orgPassword) {
		this.orgPassword = orgPassword;
	}


	public String getOrgRequest() {
		return orgRequest;
	}


	public void setOrgRequest(String orgRequest) {
		this.orgRequest = orgRequest;
	}

}
